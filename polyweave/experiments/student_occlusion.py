"""Occlusion sensitivity on a *real* CIFAR student, overlaid on the input image.

This is the mechanistic-confirmation figure for the paper (§5.5): take one trained
unseen student, install its ``conv1`` filters as generated by each teacher — the
additive ``conv`` teacher and the multiplicative ``conv_sigmapi`` teacher — and
compare how the resulting classifier's confidence depends, region-by-region, on
the input image (Zeiler & Fergus occlusion sensitivity).

Nothing is retrained: the teachers and students are loaded from the
``models/seed{N}/conv1_models.pt`` payload. That payload is *not* shipped with the
repo (it is large and regenerable); produce it first by running the conv1
experiment with model-saving enabled::

    python -m polyweave.experiments.multiseed          # saves seed-42 models

(``--save-models-dir`` defaults to ``models``; for a single seed only, call
:func:`polyweave.experiments.cifar_conv1.run` with ``Config(save_models_dir="models")``).
The only work here is weight generation (a forward pass through each teacher), a
BatchNorm re-estimation, and the occlusion sweep.

The honest expectation (see §5.5): because the Sigma-Pi pi branch is *signed-log*
(additive in log-space), the additive- and Sigma-Pi-generated ``conv1`` may yield
*similar* input-space occlusion maps. That similarity is itself reportable — the
clean linear-space AND-signature lives in genuine products (attention Q·K), not
in the signed-log conv branch.

Run:  python -m polyweave.experiments.student_occlusion --seed 42
"""

from __future__ import annotations

import argparse
import copy
import sys
from typing import Dict, List, Optional, Tuple

import torch
import torch.nn as nn

from ..evaluation import generate_averaged
from ..hypernets import ConvFilterTeacher
from ..interpretability import occlusion_sensitivity_2d
from ..prototypes import image_grid_stats
from ..students import make_cnn_student
from ..targets import Conv2dTargetSpec
from ..viz import plot_occlusion_overlay
from . import _common
from .cifar_conv1 import CONV1_IN, CONV1_KERNEL, CONV1_OUT

# CIFAR-10 class names, for labelling the predicted class in the figure title.
CIFAR10_CLASSES = ["airplane", "automobile", "bird", "cat", "deer",
                   "dog", "frog", "horse", "ship", "truck"]


# ---------------------------------------------------------------------------
# Response function: how strongly the classifier "votes" for a target class
# ---------------------------------------------------------------------------

def _class_logit_response(model: nn.Module, target_class: int):
    """A response function: the model's logit for ``target_class`` per item.

    Uses the student's *installed* ``conv1`` (``gen_conv1=None``), so it reflects
    whichever generated filters were copied in. Returns ``[N]``.
    """
    @torch.no_grad()
    def fn(x: torch.Tensor) -> torch.Tensor:
        model.eval()
        return model(x)[:, target_class]
    return fn


# ---------------------------------------------------------------------------
# Install generated conv1 + re-estimate bn1 (mirrors cifar_conv1._reset_bn1)
# ---------------------------------------------------------------------------

@torch.no_grad()
def _install_conv1(student: nn.Module, gen: Dict[str, torch.Tensor],
                   support_batches, bn_reset_batches: int) -> nn.Module:
    """Deep-copy ``student``, copy generated ``conv1`` weights in, re-estimate bn1."""
    model = copy.deepcopy(student)
    model.conv1.weight.copy_(gen["weight"])
    model.conv1.bias.copy_(gen["bias"])
    model.train()
    model.bn1.reset_running_stats()
    for i, (x, _y) in enumerate(support_batches):
        if i >= bn_reset_batches:
            break
        model(x)  # gen_conv1 None -> uses the just-installed conv1
    model.eval()
    return model


# ---------------------------------------------------------------------------
# Core: per-teacher occlusion maps over one image (testable, no I/O)
# ---------------------------------------------------------------------------

def student_occlusion_maps(
    student: nn.Module,
    teachers: Dict[str, nn.Module],
    support_batches,
    image: torch.Tensor,
    *,
    num_classes: int,
    proto_grid: int,
    window: int = 7,
    stride: int = 2,
    bn_reset_batches: int = 10,
    target_class: Optional[int] = None,
) -> Tuple[Dict[str, torch.Tensor], Dict[str, int]]:
    """Relative occlusion-sensitivity map over ``image`` for each teacher's conv1.

    For every ``(label, teacher)`` in ``teachers`` the student's ``conv1`` is
    replaced by weights the teacher generates from the support set, bn1 is
    re-estimated, and a relative occlusion map (response *fraction*) is computed
    over ``image`` w.r.t. the predicted class logit.

    Args:
        student: a trained CNN student (its trunk is reused; conv1 is swapped).
        teachers: label -> :class:`ConvFilterTeacher` (e.g. ``conv`` / ``conv_sigmapi``).
        support_batches: ``[(x, y), ...]`` used both for the generation prototype
            and the bn1 re-estimation.
        image: a single image ``[1, 3, H, W]`` (normalised, as the student expects).
        num_classes, proto_grid: prototype geometry (from the conv1 ``Config``).
        window, stride: occlusion patch size / step over the input.
        bn_reset_batches: how many support batches to use for bn1 re-estimation.
        target_class: class logit to probe; ``None`` uses each model's own
            prediction (so the map explains *that model's* decision).

    Returns:
        ``(maps, classes)`` where ``maps[label]`` is an ``[Hout, Wout]`` relative
        occlusion map and ``classes[label]`` is the probed class index.
    """
    def build_proto(_student, batch):
        x, y = batch
        return image_grid_stats(x, y, num_classes, grid=proto_grid)

    maps: Dict[str, torch.Tensor] = {}
    classes: Dict[str, int] = {}
    for label, teacher in teachers.items():
        gen = generate_averaged(teacher, student, support_batches, build_proto)
        model = _install_conv1(student, gen, support_batches, bn_reset_batches)
        cls = target_class
        if cls is None:
            with torch.no_grad():
                cls = int(model(image).argmax(1).item())
        resp = _class_logit_response(model, cls)
        m = occlusion_sensitivity_2d(
            resp, image, baseline=0.0, window=window, stride=stride, relative=True,
        )
        maps[label] = m[0]  # single image -> [Hout, Wout]
        classes[label] = cls
    return maps, classes


# ---------------------------------------------------------------------------
# De-normalisation for display
# ---------------------------------------------------------------------------

def _denormalize(image: torch.Tensor) -> torch.Tensor:
    """Undo CIFAR-10 normalisation: ``x * std + mean`` -> ``[3, H, W]`` in ~[0,1]."""
    mean = torch.tensor(_common.CIFAR10_MEAN).reshape(3, 1, 1)
    std = torch.tensor(_common.CIFAR10_STD).reshape(3, 1, 1)
    img = image.detach().cpu()
    if img.ndim == 4:
        img = img[0]
    return img * std + mean


# ---------------------------------------------------------------------------
# Driver: load saved models + real CIFAR, write the overlay figure
# ---------------------------------------------------------------------------

def _rebuild_teachers(payload: dict, device: str) -> Dict[str, ConvFilterTeacher]:
    cfg = payload["cfg"]
    spec = Conv2dTargetSpec(CONV1_OUT, CONV1_IN, CONV1_KERNEL)
    teachers: Dict[str, ConvFilterTeacher] = {}
    for kind, sigma_pi in (("conv", False), ("conv_sigmapi", True)):
        t = ConvFilterTeacher(
            spec, proto_channels=cfg["proto_channels"], width=cfg["teacher_width"],
            sigma_pi=sigma_pi, dropout=cfg.get("dropout", 0.0),
        ).to(device)
        t.load_state_dict({k: v.to(device) for k, v in payload["teachers"][kind].items()})
        t.eval()
        teachers[kind] = t
    return teachers


def _rebuild_unseen_student(payload: dict, device: str) -> nn.Module:
    """Rebuild the first unseen student (the held-out architecture group)."""
    cfg, arch = payload["cfg"], payload["arch"]
    unseen_arch = arch["archs"][arch["num_train_groups"]]  # first held-out arch
    student = make_cnn_student(
        unseen_arch, feature_dim=cfg["feature_dim"], num_classes=cfg["num_classes"],
        in_ch=CONV1_IN, conv1_out=CONV1_OUT, kernel_size=CONV1_KERNEL,
    ).to(device)
    student.load_state_dict({k: v.to(device) for k, v in payload["unseen_students"][0].items()})
    student.eval()
    return student


def _pick_image(student: nn.Module, eval_batches, device: str,
                skip: int = 0) -> Tuple[torch.Tensor, int]:
    """Pick a correctly-classified test image, skipping the first ``skip`` hits.

    ``skip`` lets the caller cycle through alternative images (e.g. to compare a
    fuzzy subject like a cat against a cleaner-edged subject like a vehicle, which
    reads crisper at 32x32) without retraining anything.
    """
    seen = 0
    for x, y in eval_batches:
        with torch.no_grad():
            pred = student(x).argmax(1)
        for i in (pred == y).nonzero(as_tuple=True)[0].tolist():
            if seen == skip:
                return x[i:i + 1].to(device), int(y[i].item())
            seen += 1
    x, y = eval_batches[0]
    return x[:1].to(device), int(y[0].item())


def main(argv: Optional[List[str]] = None) -> None:
    sys.stdout.reconfigure(encoding="utf-8")  # Windows cp1252 safety
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--models-dir", default="models")
    ap.add_argument("--window", type=int, default=7)
    ap.add_argument("--stride", type=int, default=2)
    ap.add_argument("--image-index", type=int, default=0,
                    help="which correctly-classified test image to use (0 = first); "
                         "non-zero writes to a suffixed filename so figures coexist.")
    ap.add_argument("--device", default=None)
    args = ap.parse_args(argv)

    device = args.device or ("cuda" if torch.cuda.is_available() else "cpu")
    path = f"{args.models_dir}/seed{args.seed}/conv1_models.pt"
    print(f"loading {path}")
    payload = torch.load(path, map_location=device, weights_only=False)

    teachers = _rebuild_teachers(payload, device)
    student = _rebuild_unseen_student(payload, device)
    cfg = payload["cfg"]

    train_loader, test_loader = _common.cifar10_loaders(cfg["batch_size"])
    support = _common.collect_batches(train_loader, cfg["eval_support_batches"], device)
    eval_batches = _common.collect_batches(test_loader, 4, device)
    image, true_cls = _pick_image(student, eval_batches, device, skip=args.image_index)

    maps, classes = student_occlusion_maps(
        student, teachers, support, image,
        num_classes=cfg["num_classes"], proto_grid=cfg["proto_grid"],
        window=args.window, stride=args.stride,
        bn_reset_batches=cfg.get("bn_reset_batches", 10),
    )

    pretty = {_common.method_label(k): v for k, v in maps.items()}
    label = CIFAR10_CLASSES[true_cls] if true_cls < len(CIFAR10_CLASSES) else str(true_cls)
    _common.configure_plots(cfg.get("dark_plots", False))
    suffix = "" if args.image_index == 0 else f"_img{args.image_index}"
    plot_occlusion_overlay(
        _denormalize(image), pretty,
        name=f"polyweave_cifar_conv1_student_occlusion_seed{args.seed}{suffix}",
        title=f"Conv1 occlusion sensitivity (input class: {label})",
    )
    print("predicted classes per teacher:",
          {k: CIFAR10_CLASSES[c] for k, c in classes.items()})
    print("Done.")


if __name__ == "__main__":
    main()
